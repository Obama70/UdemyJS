//operadores logicos
//&&(and), ||(or), !(not).
let a = true;
let b = false;
console.log(a);
console.log(b);

//operador logico and(&&)
//regresa verdadero si ambos operandos son verdaderos 
console.log(`${a} && ${b} -> ${a&&b}`)

//operador logico or(||)
//regresa verdadero si cualquiera de los dos operandos son verdaderos
console.log(`${a} || ${b} -> ${a||b}`)

//operador logico not(!)
//convierte cualquier operando a su contrario notTrue notFalse
console.log(`${a} ! ${!a}, ${b} ! ${!b}`)