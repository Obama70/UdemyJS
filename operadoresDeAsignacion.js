//operadores de asignacion 

let miNumero =10;
console.log(miNumero);

miNumero = 15;
console.log(miNumero)

//operador asignacion compuesto 
miNumero += 5;
console.log(miNumero)

//operador -=
miNumero -=3;
console.log(miNumero)

// operador *=
miNumero *= 2;
console.log(miNumero)

// operador /=
miNumero /= 4;
console.log(miNumero)

//operador %=
miNumero %= 2;
console.log(miNumero)

//exponente **=

miNumero **= 2;
console.log(miNumero)
